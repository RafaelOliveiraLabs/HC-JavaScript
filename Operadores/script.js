//operadores aritmeticos

// add +
var sumTwoNumbers = 4 + 3;
console.log(sumTwoNumbers);

// sub -
var subTwoNumbers = 4 - 3;
console.log(subTwoNumbers);

// mult *
var multTwoNumbers = 4 * 3;
console.log(multTwoNumbers);

//div /
var divTwoNumbers = 4 / 3;
console.log(divTwoNumbers);

//resto ou modulo de divisao %
var restoTwoNumbers = 4 % 3;
console.log(restoTwoNumbers);

//incremento ++
var num = 1;
console.log(++num);

//decremento --
console.log(--num);

